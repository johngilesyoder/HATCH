  <p>Spawned out of a program hosted by NASA, Urban Matrix One is utilizing satellite imagery, machine learning, and unique data sets to quantify the impact of projects focused on the built environment. We are working to utilize appropriate technologies to better inform capital markets to invest in more regenerative and sustainable projects through minimizing risk and making SDG impact explainable. 


Being able to quantify the impact of complex global systems is a challenge. Through the utilization of current and edge technologies how do we holistically quantify and empower capital markets to make better decisions in identifying and quantifying built environment investments?  

Spawned out of Singularity University in collaboration with NASA- Urban Matrix One created a company to harness satellite imagery, machine learning and unique data sets to help innovators, developers and capital markets make fast, actionable decisions concerning people, places, and things. In much of the world there are missing data layers in which can be a real challenge to identify the correlations between investments and impact of the UN Sustainable goals. 

The past 18 months we have been working on creating applications that will help Institutional Investors, Project Developers, NGOs, and Municipalities gather insightful correlations to economic, social and environmental categories. Through these insights and the inevitable challenge of climate change, we are creating tools for innovators and practitioners to make better decisions by utilizing explainable AI that takes into account multiple layers and holistic view of risk when funding built environment projects. 

